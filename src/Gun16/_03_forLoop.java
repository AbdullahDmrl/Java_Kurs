package Gun16;

public class _03_forLoop {
    public static void main(String[] args) {

        // 0 dan 10 dahil sayilari ekrana yazdiriniz

        System.out.print("0 dan 10 sayilar=");

        for(int i=0;i<=10;i++)
        {
            System.out.print(i+" ");}

        // 10 dan 0 dahil sayilari ekrana yazdiriniz

        System.out.println();

        System.out.print("10 dan 0 sayilar=");

        for (int j=10;j>=0;j--)
        {
            System.out.print(j+" ");}



    }
}
