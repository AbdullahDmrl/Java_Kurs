package Gun17;

public class _02_javaNestedLoop {
    public static void main(String[] args) {
        // asagidaki sekilde cikti veren programi
        // print icinde sadece bir tane # kullanarak yazdiriniz
        // #####
        // #####
        // #####
        // #####


        for (int i=1; i<5;i++)    // sutun yazar
        {
            for (int j=1; j<5;j++)     // satir yazar
                System.out.print("#");
            System.out.println(); // aralarina bosluk birakmak icin
        }


    }
}
