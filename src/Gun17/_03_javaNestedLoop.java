package Gun17;

public class _03_javaNestedLoop {
    public static void main(String[] args) {

        // 1*1=1    carpim tablosunu 10 a kadar yaziniz
        // 1*2=2

        for (int i=1; i<=10; i++)
        {
            for (int j=1;j<=10;j++)
                System.out.println(i+"*"+j+"="+(i*j));
            System.out.println();
        }


    }
}
